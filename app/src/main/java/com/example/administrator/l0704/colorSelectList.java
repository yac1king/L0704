package com.example.administrator.l0704;

public class colorSelectList {
    String colorName;
    String colorCode;

    public colorSelectList(String colorName, String colorCode) {
        this.colorName = colorName;
        this.colorCode = colorCode;
    }

    public String getColorName() {
        return colorName;
    }

    public String getColorCode() {
        return colorCode;
    }
}
